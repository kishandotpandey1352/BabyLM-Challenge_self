import abc
from typing import List, Tuple, Optional


class BaseLM(abc.ABC):
    @abc.abstractmethod
    def loglikelihood(self, requests: List[Tuple[str, str]]) -> List[Tuple[float, bool]]:
        """
        Returns the log likelihoods of each continuation in the context of the prompt.

        :param requests: A list of (context, continuation) pairs.
        :return: A list of tuples of the form (logprob, is_greedy), where:
            - logprob is the log probability of the continuation given the context.
            - is_greedy is a boolean indicating whether the continuation was the model's top choice.
        """
        pass

    @abc.abstractmethod
    def loglikelihood_rolling(self, requests: List[str]) -> List[float]:
        """
        Returns the rolling log likelihoods for a list of strings.

        :param requests: A list of strings.
        :return: A list of log likelihoods, one for each string.
        """
        pass

    @abc.abstractmethod
    def greedy_until(self, requests: List[Tuple[str, List[str]]]) -> List[str]:
        """
        Generate text using greedy decoding until one of the stop sequences is encountered.

        :param requests: A list of (context, until) pairs.
        :return: A list of strings generated by the model.
        """
        pass

    @property
    def eot_token_id(self) -> int:
        """
        Returns the ID of the end-of-text token.
        Override if your model has a different EOT token.
        """
        return None

    @property
    def max_length(self) -> int:
        """
        Returns the maximum number of tokens that the model can handle in a single forward pass.
        """
        return 2048

    @property
    def batch_size(self) -> int:
        """
        Returns the maximum batch size to use during evaluation.
        Override this if your model needs smaller batch sizes.
        """
        return 1

    @property
    def device(self) -> str:
        """
        Returns the device the model is operating on.
        """
        return "cuda"  # or "cpu"
